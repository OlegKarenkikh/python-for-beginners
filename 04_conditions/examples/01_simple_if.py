# Глава 04 — Простые условия
age = int(input("Введите возраст водителя: "))

if age < 18:
    print("❌ Нельзя оформить полис — нет прав")
elif age < 25:
    print("⚠️  Молодой водитель — коэффициент 1.5")
elif age > 70:
    print("⚠️  Возрастной водитель — коэффициент 1.3")
else:
    print("✅ Стандартный клиент — коэффициент 1.0")
