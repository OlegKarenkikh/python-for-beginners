# Глава 05 — Цикл for: основы
clients = [
    ("Иванов А.П.", 35, 12_000),
    ("Петрова М.С.", 22, 18_000),
    ("Сидоров В.Н.", 45, 10_500),
    ("Козлова Е.В.", 31, 14_200),
]

print(f"{'Клиент':<25} {'Возраст':>7} {'Премия':>10}")
print("-" * 45)
for name, age, premium in clients:
    print(f"{name:<25} {age:>7} {premium:>10,} руб.")

total = sum(p for _, _, p in clients)
print("-" * 45)
print(f"{'ИТОГО':>32} {total:>10,} руб.")
