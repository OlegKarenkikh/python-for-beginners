# Глава 02 — Обработка имён из базы
# В реальной базе данных имена часто вводятся
# с ошибками — пробелами, разным регистром

raw_names = [
    "  ИВАНОВ алексей  ",
    "петрова МАРИЯ сергеевна",
    " СИДОРОВ виктор ",
]

print("Нормализованные имена:")
for raw in raw_names:
    clean = raw.strip().title()
    print(f"  {raw!r:35} → {clean}")
