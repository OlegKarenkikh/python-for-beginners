# Глава 01 — Пример 1: Первые переменные
# Запустите: python examples/01_first_variables.py

# Создаём переменные
name = "Иванов Алексей Петрович"
age = 35
city = "Москва"

# Выводим на экран
print("Имя:", name)
print("Возраст:", age)
print("Город:", city)

# f-строка — современный способ форматирования
print(f"\nКлиент: {name}, {age} лет, г. {city}")
